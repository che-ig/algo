# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution:
    # time: O(n + m), (n, m) - длины списков
    # mem (доп): O(1)
    def getIntersectionNode(self, headA: ListNode, headB: ListNode) -> ListNode | None:
        p1 = headA
        p2 = headB
        # рассмотрим почему это работает:
        # 1 - если списки одинаковой длины и не имеет пересечений, то получим None
        # 2 - если списки одинаковой длины и имеет пересечение, то в каждый раз проверяем
        #     пересекаются ли списки и гарантированно получим первую ноду, где пересеклись
        # 3 - списки не равной длины и не пересекаются -> получим None т к p1 пройдет в сумме
        #     len(headA) + len(headB) и p2 пройдет столько же и оба будут показывать на None
        # 4 - списки не равной длины и пересекаются. Тут мы тоже получим, что p1 и p2 пройдут
        #     одинаковое число шагов, т к оба идет сначала по headA, а потом headB, значит
        #     они будут иметь одинаковый отступ и будут выровнены и придут одновременно
        # Note: словами может быть непонятно, поэтому нарисуйте 4 этих случая на бумаге
        # и посмотрите как это работает
        while p1 != p2:
            p1 = p1.next if p1 is not None else headB
            p2 = p2.next if p2 is not None else headA
        return p1  # возвращаем или p1 или p2


"""
Суть приема

Идея: Если мы заставим два указателя пройти одинаковое количество узлов, они одновременно окажутся в точке пересечения (или в конце списков).
Пошаговое объяснение

    Инициализация: Два указателя p1 и p2 начинают с голов соответствующих списков.

    Обход: Пока указатели не встретятся (p1 != p2):

        Если p1 достиг конца списка A, он переключается на голову списка B

        Если p2 достиг конца списка B, он переключается на голову списка A

        Иначе каждый просто переходит к следующему узлу

    Завершение: Когда указатели встречаются, они указывают либо на:

        Узел пересечения (если он есть)

        None (если пересечения нет)

Почему это работает?

Математическое обоснование:

Пусть:

    lenA = длина списка A

    lenB = длина списка B

    lenC = длина общей части (пересечения)

Тогда:

    Расстояние до пересечения из A = lenA - lenC

    Расстояние до пересечения из B = lenB - lenC

Когда указатели проходят свои списки и переключаются:

    p1 пройдет: (lenA - lenC) + lenC + (lenB - lenC) = lenA + lenB - lenC

    p2 пройдет: (lenB - lenC) + lenC + (lenA - lenC) = lenA + lenB - lenC

Расстояния одинаковые! Поэтому они встретятся в точке пересечения.
Визуальный пример
text

Список A: 1 → 2 → 3
                  ↓
Список B: 4 → 5 → 6 → 3 → 7 → 8

Указатели:

    p1 начинает с 1, p2 с 4

    p1 проходит: 1→2→3→7→8→None→(переключается на B)→4→5→6

    p2 проходит: 4→5→6→3→7→8→None→(переключается на A)→1→2→3

Они встретятся на узле 3!
Сложность

    Время: O(n + m), где n и m - длины списков

    Память: O(1) - используется только два указателя
"""
